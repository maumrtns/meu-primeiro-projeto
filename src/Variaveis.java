public class Variaveis {

    static final float VALOR_PI = 3.14f;

    public static void main(String[] args) {
        // variável é um valor volátil

        int idade;
        idade = 25;

        // constante PI -> 3,14

        System.out.println("A minha idade é: " + idade);

        System.out.println("Valor do PI é: " + VALOR_PI);




    }
}
